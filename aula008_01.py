# digitando import e precione a tecla( Ctrl )e aperte a ( barra de espaço ),
#  para mostrar todas as funcionalidades

from math import sqrt,floor

num = int(input('Digite um numero: '))
raiz = sqrt(num)
print('A raiz quadrada de {} é igual a {:.2f}'.format(num,floor(raiz)))


# exemplo: escrever 6,34 e mostrar somente o 6

import math
num = float(input('Escreva um numero inteiro, com duas casa decimais: '))
valor = math.floor(num)
print('O numero é: {}' .format(math.floor(valor)))
